package ne.edu.est.gestion_location.entities;

public class Garant {
    private int idGarant;
    private String designationGarant;
    private String valeurGarant;

    public int getIdGarant() {
        return idGarant;
    }

    public void setIdGarant(int idGarant) {
        this.idGarant = idGarant;
    }

    public String getDesignationGarant() {
        return designationGarant;
    }

    public void setDesignationGarant(String designationGarant) {
        this.designationGarant = designationGarant;
    }

    public String getValeurGarant() {
        return valeurGarant;
    }

    public void setValeurGarant(String valeurGarant) {
        this.valeurGarant = valeurGarant;
    }
}
